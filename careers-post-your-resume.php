function fileUpload(form, action_url, div_id)
{

var d=document.forms["resume"]["title"].value;
if (d==null || d=="")
  {
  alert("Title must be filled out");
  return false;
  }

var a=document.forms["resume"]["uname"].value;
if (a==null || a=="")
  {
  alert("Name must be filled out");
  return false;
  }

var c=document.forms["resume"]["job"].value;
if (c==null || c=="")
  {
  alert("Job must be filled out");
  return false;
  }

var g=document.forms["resume"]["city"].value;
if (g==null || g=="")
  {
  alert("City must be filled out");
  return false;
  }


var b=document.forms["resume"]["emailid"].value;
var atpos=b.indexOf("@");
var dotpos=b.lastIndexOf(".");
if (atpos<1 || dotpos<atpos+2 || dotpos+2>=b.length)
  {
  alert("Not a valid e-mail address");
  return false;
  }

var h=document.forms["resume"]["mobile"].value;
if (h==null || h=="")
  {
  alert("Mobile must be filled out");
  return false;
  }

var f=document.forms["resume"]["datafile"].value;
if (f==null || f=="")
  {
  alert("Please upload file");
  return false;
  }

var ext = jQuery('#datafile').val().split('.').pop().toLowerCase();
if(jQuery.inArray(ext, ['doc','docx','pdf','txt','rtf']) == -1) {
    alert('invalid extension!');
return false;
}
// Create the iframe...
document.getElementById('submit').style.display="none";
document.getElementById('upload').style.display="block";
var iframe = document.createElement("iframe");
iframe.setAttribute("id","upload_iframe");
iframe.setAttribute("name","upload_iframe");
iframe.setAttribute("width","0");
iframe.setAttribute("height","0");
iframe.setAttribute("border","0");
iframe.setAttribute("style","width: 0; height: 0; border: none;");
 
// Add to document...
form.parentNode.appendChild(iframe);
window.frames['upload_iframe'].name="upload_iframe";
 
iframeId = document.getElementById("upload_iframe");
 
// Add event...
var eventHandler = function()  {
 
if (iframeId.detachEvent)
iframeId.detachEvent("onload", eventHandler);
else
iframeId.removeEventListener("load", eventHandler, false);
 
// Message from server...
if (iframeId.contentDocument) {
content = iframeId.contentDocument.body.innerHTML;
} else if (iframeId.contentWindow) {
content = iframeId.contentWindow.document.body.innerHTML;
} else if (iframeId.document) {
content = iframeId.document.body.innerHTML;
}
 

document.getElementById(div_id).innerHTML = content;
 
// Del the iframe...
setTimeout('iframeId.parentNode.removeChild(iframeId)', 250);
}
 
if (iframeId.addEventListener)
iframeId.addEventListener("load", eventHandler, true);
if (iframeId.attachEvent)
iframeId.attachEvent("onload", eventHandler);
 
// Set properties of form...
form.setAttribute("target","upload_iframe");
form.setAttribute("action", action_url);
form.setAttribute("method","post");
form.setAttribute("enctype","multipart/form-data");
form.setAttribute("encoding","multipart/form-data");
 
// Submit the form...
form.submit();
 
alert("Thank you!");

}
