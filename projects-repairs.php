<h4>Specifications and Proposals Service</h4>
<p>Our Specifications and Proposals Service includes a site visit, visual inspection, testing where required, and finally, preparing a repairs proposal with a cost and time estimate and an offer to carry out the repairs.<br />
We believe that the only way forward to carry on a successful remedial undertaking regarding Repairs & Rehabilitation and Waterproofing is accurate, timely and quality diagnosis of the problem. A quality survey is the foundation for all future decisions about the structure.<br />

Our Proposals Engineers are distinguished by their expertise, experience and motivation to carry out extremely detailed visual surveys. Geological, physical and financial parameters, are some of the pointers of any project that are kept in mind while approaching the job. This added effort saves our clients wasteful expenditure of money, by letting us identifying the most economic and effective method of repairing the concrete.
Our detailed surveys are often used by clients, surveyors and engineers as tendering documents, allowing the client to have realistic refurbishment costs without the fear of additional works and spiraling costs.</p>

<h4>Project Execution </h4>

<p>Once the survey, specifications and proposals are completed, we at BDS implement our  services as Principal Contractor to execute the project, whether large or small, as per requirements and client specifications.<br />

Our services are available at any location in India and UAE. BDS has the capability and logistical resources to manage project-related works on- and off-site, with resource mobilisation keeping in stride with the project. We at BDS have the necessary equipment, supplementary force and mechanical resource to undertake multiple projects of different shape and size.<br />

BDS boasts of a team of highly experienced, skilled, talented and motivated in-house engineers, supervisors and equally skilled workers.<br />

Our engineers and supervisors carry out dedicated on-site monitoring of Quality Assurance parameters, ensuring the satisfactory completion of every project.<br />

In any project, the on-site handling of construction chemicals and products requiring highly specialized application techniques plays a vital role in the overall execution quality. The BDS on-site team carefully studies all such products, their application procedures, limitations, precautions required etc, and ensures that the workers who apply these products have the requisite skill and training.<br />

BDS does not compromise on safety, and invests in all requisite safety equipment and gear. Our safety officers have been trained at respected institutes like the National Institute of Fire Engineering. Their responsibility extends to both human resource, and equipment. The company also ensures that adequate insurance cover is taken through the workers compensation insurance policy and CAR (contractors all risk) policy.<br />

The commitment and care of our team ensures that we succeed, even where others fail- thus making us one of the reliable best in the industry of concrete engineering. Collectively, we have on-board the skill to complete layered, complex and demanding projects of any scale – ranging in scope from less than a million rupees to several tens of millions.</p>


